package aula2403_;

public class Pessoas {
	//atributos da classe >> caracteristicas
	// privados // encapsulamento
	private int idade;
	private String nome;	
	// getters e setters
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}
